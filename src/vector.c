////////////////////////////////////////////////////////////////////////////////
// Funksjon som beregner størrelsen på en 2D vektor
////////////////////////////////////////////////////////////////////////////////
float vec2_length(vec2_t v) {
	return sqrt(v.x * v.x + v.y * v.y);
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som adderer to 2D vektorer
////////////////////////////////////////////////////////////////////////////////
vec2_t vec2_add(vec2_t a, vec2_t b) {
	vec2_t v = { 
		.x = a.x + b.x,
		.y = a.y + b.y
	};
	return v;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som subtraherer to 2D vektorer
////////////////////////////////////////////////////////////////////////////////
vec2_t vec2_sub(vec2_t a, vec2_t b) {
	vec2_t v = { 
		.x = a.x - b.x,
		.y = a.y - b.y
	};
	return v;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som multipliserer en 2D vektor med en skalar
////////////////////////////////////////////////////////////////////////////////
vec2_t vec2_mul(vec2_t v, float factor) {
	vec2_t res = { 
		.x = v.x * factor,
		.y = v.y * factor
	};
	return res;

}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som deler en 2D vektor med en skalar
////////////////////////////////////////////////////////////////////////////////
vec2_t vec2_div(vec2_t v, float factor) {
	vec2_t res = { 
		.x = v.x / factor,
		.y = v.y / factor
	};
	return res;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som finner dot-produktet av to 2D vektorer
////////////////////////////////////////////////////////////////////////////////
float vec2_dot(vec2_t a, vec2_t b) {
	return (a.x * b.x) + (a.y * b.y);
}

/* Implementasjon av 3D vektor funksjoner */

////////////////////////////////////////////////////////////////////////////////
// Funksjon som beregner størrelsen på en 3D vektor
////////////////////////////////////////////////////////////////////////////////
float vec3_length(vec3_t v) {
	return sqrt(v.x * v.x + v.y * v.y + v.z * v.z);
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som adderer to 3D vektorer
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_add(vec3_t a, vec3_t b) {
	vec3_t v = { 
		.x = a.x + b.x,
		.y = a.y + b.y,
		.z = a.z + b.z
	};
	return v;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som subtraherer to 3D vektorer
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_sub(vec3_t a, vec3_t b) {
	vec3_t v = { 
		.x = a.x - b.x,
		.y = a.y - b.y,
		.z = a.z - b.z
	};
	return v;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som multipliserer en 3D vektor med en skalar
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_mul(vec3_t v, float factor) {
	vec3_t res = { 
		.x = v.x * factor,
		.y = v.y * factor,
		.z = v.z * factor
	};
	return res;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som deler en 3D vektor med en skalar
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_div(vec3_t v, float factor) {
	vec3_t res = { 
		.x = v.x / factor,
		.y = v.y / factor,
		.z = v.z / factor
	};
	return res;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som finner normalen (dvs. kryssproduktet) av to 3D vektorer
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_cross(vec3_t a, vec3_t b) {
	vec3_t res = {
		.x = a.y * b.z - a.z * b.y,
		.y = a.z * b.x - a.x * b.z,
		.z = a.x * b.y - a.y * b.x
	};
	return res;
}

////////////////////////////////////////////////////////////////////////////////
// Funksjon som finner dot-produktet av to 3D vektorer
////////////////////////////////////////////////////////////////////////////////
float vec3_dot(vec3_t a, vec3_t b) {
	return (a.x * b.x) + (a.y * b.y) + (a.z * b.z);
}

////////////////////////////////////////////////////////////////////////////////
/// Funksjon som mottar en 3D vektor og returnerer et projektert 2D punkt
/// Vi trenger å bruke projeksjon for å vise et 3D objekt på en 2D skjermflate
/// Orthographisk projeksjon: alle sider er like lange uavhengig avstand til 
/// kamera (ingen dybde eller perspektiv)
/// Perspektiv projeksjon legger på dybde
////////////////////////////////////////////////////////////////////////////////
vec2_t project(vec3_t v, float fov_factor) {
	vec2_t projected_point = {
		.x = (fov_factor * v.x) / v.z, // Skalerer og legger på dybde effekt
		.y = (fov_factor * v.y) / v.z
	};
	return projected_point;
}

////////////////////////////////////////////////////////////////////////////////
/// Funksjon som roterer en 3D vektor rundt x-aksen
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_rotate_x(vec3_t v, float angle) {
	vec3_t rotated_vector = {
		.x = v.x, // Holdes konstant
		.y = v.y * cos(angle) - v.z * sin(angle),
		.z = v.y * sin(angle) + v.z * cos(angle)
	};
	return rotated_vector;
}

////////////////////////////////////////////////////////////////////////////////
/// Funksjon som roterer en 3D vektor rundt z-aksen
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_rotate_z(vec3_t v, float angle) {
	vec3_t rotated_vector = {
		.x = v.x * cos(angle) - v.y * sin(angle),
		.y = v.x * sin(angle) + v.y * cos(angle),
		.z = v.z // Holdes konstant

	};
	return rotated_vector;
}

////////////////////////////////////////////////////////////////////////////////
/// Funksjon som roterer en 3D vektor rundt y-aksen
////////////////////////////////////////////////////////////////////////////////
vec3_t vec3_rotate_y(vec3_t v, float angle) {
	vec3_t rotated_vector = {
		.x = v.x * cos(angle) - v.z * sin(angle),
		.y = v.y, // Holdes konstant
		.z = v.x * sin(angle) + v.z * cos(angle),

	};
	return rotated_vector;
}
